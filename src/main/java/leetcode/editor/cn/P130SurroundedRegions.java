    public void solve(char[][] board) {
        if (board == null || board.length == 0) return;
        int m = board.length;
        int n = board[0].length;
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                // ä»è¾¹ç¼˜oå¼€å§‹æœç´¢
                boolean isEdge = i == 0 || j == 0 || i == m - 1 || j == n - 1;
                if (isEdge && board[i][j] == 'O') {
                    dfs(board, i, j);
                }
            }
        }

        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                if (board[i][j] == 'O') {
                    board[i][j] = 'X';
                }
                if (board[i][j] == '#') {
                    board[i][j] = 'O';
                }
            }
        }
    }

    public void dfs(char[][] board, int i, int j) {
        if (i < 0 || j < 0 || i >= board.length  || j >= board[0].length || board[i][j] == 'X' || board[i][j] == '#') {
            // board[i][j] == '#' è¯´æ˜å·²ç»æœç´¢è¿‡äº†.
            return;
        }
        board[i][j] = '#';
        dfs(board, i - 1, j); // ä¸Š
        dfs(board, i + 1, j); // ä¸‹
        dfs(board, i, j - 1); // å·¦
        dfs(board, i, j + 1); // å³
    }


